package com.kbs.geo.coastal.model;

public class WebClientAuthResponse {
	private String kbWebToken;
	private String kbUserRole;
	
	public String getKbWebToken() {
		return kbWebToken;
	}
	public void setKbWebToken(String kbWebToken) {
		this.kbWebToken = kbWebToken;
	}
	public String getKbUserRole() {
		return kbUserRole;
	}
	public void setKbUserRole(String kbUserRole) {
		this.kbUserRole = kbUserRole;
	}
}
